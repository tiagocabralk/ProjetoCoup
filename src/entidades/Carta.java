package entidades;

public class Carta {

    private Duque duque;
    private Assassino assassino;
    private Condessa condessa;
    private Capitao capitao;
    private Embaixador embaixador;
    private Inquisitor inquisitor;

    public Carta() {
    }

    public Duque getDuque() {
        return duque;
    }

    public void setDuque(Duque duque) {
        this.duque = duque;
    }

    public Assassino getAssassino() {
        return assassino;
    }

    public void setAssassino(Assassino assassino) {
        this.assassino = assassino;
    }

    public Condessa getCondessa() {
        return condessa;
    }

    public void setCondessa(Condessa condessa) {
        this.condessa = condessa;
    }

    public Capitao getCapitao() {
        return capitao;
    }

    public void setCapitao(Capitao capitao) {
        this.capitao = capitao;
    }

    public Embaixador getEmbaixador() {
        return embaixador;
    }

    public void setEmbaixador(Embaixador embaixador) {
        this.embaixador = embaixador;
    }

    public Inquisitor getInquisitor() {
        return inquisitor;
    }

    public void setInquisitor(Inquisitor inquisitor) {
        this.inquisitor = inquisitor;
    }
}
